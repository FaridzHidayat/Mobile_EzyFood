package com.example.ezyfood;

public class Drink {
    String name;
    Integer price;
    Integer photo;

    public String getName(){
        return name;
    }

    public void setName(String name){
        this.name=name;
    }

    public int getPrice(){
        return price;
    }

    public void setPrice(Integer price){
        this.price=price;
    }

    public int getPhoto(){
        return photo;
    }

    public void setPhoto(Integer photo){
        this.photo=photo;
    }

}
